bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {

    case KSWITCH: // send sequence for USB hub to switch ports.
      if (record->event.pressed) {
          rgblight_blink_layer_repeat(SWAP, 200, 5);
          SEND_STRING(SS_TAP(X_LCTL) SS_TAP(X_LCTL) SS_TAP(X_ENTER));
      }
      return false;
      break;
  }
  return true;
}

void keyboard_post_init_user(void) {
    rgblight_layers = my_light_layers;
}

layer_state_t default_layer_state_set_user(layer_state_t state) {
    rgblight_set_layer_state(QWERTY, true);

    return state;
}

layer_state_t layer_state_set_user(layer_state_t state) {
    rgblight_set_layer_state(FN, layer_state_cmp(state, FN));
    rgblight_set_layer_state(LOWER, layer_state_cmp(state, LOWER));
    rgblight_set_layer_state(RAISE, layer_state_cmp(state, RAISE));
    rgblight_set_layer_state(CMD, layer_state_cmp(state, CMD));
    rgblight_set_layer_state(SWAP, layer_state_cmp(state, SWAP));

    return state;
}
